    <p dir="ltr"><strong>The Game</strong></p>
    <p dir="ltr">We Go Forth is a four player cooperative dungeon crawler that puts the player in a party of four with the choice of six classes, each with a unique play style and ability set. </p>
    <p dir="ltr">Players take control of one of six elemental entities long grown bored of their mastery of the natural order. They seek a new challenge, and find their way to the Formless Place, a dimension of challenge, struggle and triumph. Communication and teamwork is impertive, as without it they will never return alive.</p>
    <p dir="ltr">Learn from each successive playthrough how best to form your party, combat tactical situations and conquer each dungeon with the goal of pushing your party further into the the Formless Place than ever before. Gather your party, go forth and triumph.</p>
    <p dir="ltr">&nbsp;</p>
    <p dir="ltr"><strong>Game Features</strong><br />
</p>
    <ul>
      <li dir="ltr">Randomized dungeons: Play dungeons built out of a variety of handcrafted level pieces. 
      </li>
    </ul>
    <ul>
      <li> Four player local co-op: Invite your friends over for a challenging and frantic co-op experience. </li>
    </ul>
    <ul>
      <li dir="ltr">6 classes: Choose from six classes all with radically different playstyles and abilities</li>
    </ul>
    <ul>
      <li>8 enemy types: Fight against 8 enemy types in challenging tactical environments.</li>
    </ul>
    <ul>
      <li dir="ltr">Celtic inspired fantasy world: Challenge the formless place a nether realm beyond the comprehension of the natural order of the world.</li>
    </ul>
    <p>&nbsp;</p>
    <p dir="ltr"><strong>Platform</strong></p>
    <p dir="ltr">The current aim of We Go Forth is for PC with the Unity engine, however there is also the possibility of an Xbox Live Arcade version, and further platforms beyond that in the future.</p>
